#!/bin/bash

# индикация трафика через порт docker0:131*
sudo tcpdump -i docker0 -n \
  | grep -P '172\.17\.0\.\d{1,3}\.131\d' \
  | perl -pe 's/(^\d.+?IP\s)((\d{1,3}\.){4}\d+)(\s>\s)((\d{1,3}\.){4}\d+)(.+$)/\2\4\5/'
